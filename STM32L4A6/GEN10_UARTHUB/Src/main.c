void uartRxParser(void* pv){
	int port = (int)pv;
	uint8_t buf[2048];
	B_uartStart(&buarts[port], huarts[port]);
	for(;;){
		size_t len = B_uartRead(&buarts[port], buf);
		for(size_t i = 0; i < 6; i++){
			if(i == port) continue;
			B_uartWrite(&buarts[i], buf, len);
		}
	}
}
