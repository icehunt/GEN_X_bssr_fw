static void switchStateTask(void const* pv){
	GPIO_TypeDef* swPort = CAMERA_GPIO_Port;
	#define uartFrame_SIZE 7
	uint8_t uartFrame[uartFrame_SIZE] = {0xa5, 0x04, 0x00, 0x00, 0x00, 0x00, 0x00};

	for(;;){
		uartFrame[2] = (swPort->IDR >> 2) & 0xff;
		uint32_t crc = ~HAL_CRC_Calculate(&hcrc, uartFrame, uartFrame_SIZE-4);
		for(size_t i=0; i<4; i++) uartFrame[i+3] = (crc >> (i << 3)) & 0xff;
		HAL_UART_Transmit_IT(&huart2, uartFrame, uartFrame_SIZE);
		HAL_UART_Transmit_IT(&huart1, uartFrame, uartFrame_SIZE);
		osDelay(1000);
	}
}

static void ledTask(const void* pv){
	htim1.Instance->CCER = 0x444;
	HAL_TIM_PWM_Start(&htim1, TIM_CHANNEL_1);
	HAL_TIM_PWM_Start(&htim1, TIM_CHANNEL_2);
	HAL_TIM_PWM_Start(&htim1, TIM_CHANNEL_3);

	uint32_t gpio_en = 0x001;

	for(;;){
		HAL_GPIO_WritePin(CAMERA_LED_GPIO_Port, gpio_en, 1);
		gpio_en <<= 1;
		gpio_en = gpio_en > 0x100 ? 0x001 : gpio_en;
		HAL_GPIO_WritePin(CAMERA_LED_GPIO_Port, gpio_en, 0);

		osDelay(1);
	}
}
